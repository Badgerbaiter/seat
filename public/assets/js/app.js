// Make your submit button part of class confirmform, and viola
var currentForm;
$(document).on("click", ".confirmform", function(e){
   currentForm = $(this).closest("form");
   e.preventDefault();
   bootbox.confirm("Are you sure you want to continue?", function(confirmed) {
      if ( confirmed ) {
         currentForm.submit();
      }
   });
});

